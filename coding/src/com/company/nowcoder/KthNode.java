package com.company.nowcoder;

/**
 * @author xxy
 * @date 2019/6/30
 * @description
 * 给定一棵二叉搜索树，请找出其中的第k小的结点。
 * 例如， （5，3，7，2，4，6，8）    中，
 * 按结点数值大小顺序第三小结点的值为4。
 */
public class KthNode {
    TreeNode ret;
    int cnt = 0;
    TreeNode KthNode(TreeNode pRoot, int k)
    {
        inOrder(pRoot,k);
        return ret;
    }
    private void inOrder(TreeNode root, int k){
        if(root==null||cnt>=k){
            return;
        }
        inOrder(root.left,k);
        cnt++;
        if(cnt==k){
            ret=root;
        }
        inOrder(root.right,k);
    }
}
